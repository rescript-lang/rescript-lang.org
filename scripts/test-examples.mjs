import fs from "fs";
import { globSync } from "tinyglobby";
import path from "path";
import { fileURLToPath } from "url";
import child_process from "child_process";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");

const rescriptJson = `{
  "name": "temp",
  "namespace": false,
  "jsx": {
    "version": 4
  },
  "dependencies": [
    "@rescript/react"
  ],
  "warnings": {
    "number": "-109-27-32"
  },
  "sources": [
    {
      "dir": "src"
    }
  ]
}`;

let parseFile = (content) => {
  if (!/```res (example|prelude|sig)/.test(content)) {
    return;
  }

  let inCodeBlock = false;
  let moduleId = 0;

  return content
    .split("\n")
    .map((line) => {
      let modifiedLine = "";
      if (line.startsWith("```res example")) {
        inCodeBlock = true;
        modifiedLine = `/* _MODULE_EXAMPLE_START */ module M_${moduleId++} = {`;
      } else if (line.startsWith("```res prelude")) {
        inCodeBlock = true;
        modifiedLine = "/* _MODULE_PRELUDE_START */ include {";
      } else if (line.startsWith("```res sig")) {
        inCodeBlock = true;
        modifiedLine = `/* _MODULE_SIG_START */ module type M_${moduleId++} = {`;
      } else if (inCodeBlock) {
        if (line.startsWith("```")) {
          inCodeBlock = false;
          modifiedLine = "} // _MODULE_END";
        } else {
          modifiedLine = line;
        }
      }

      return modifiedLine;
    })
    .join("\n");
};

let splitLines = (content) => content.split("\n");

let fenceKind = (line) => {
  if (line.startsWith("```res example")) {
    return "res-example";
  }

  if (line.startsWith("```js") || line.startsWith("```javascript")) {
    return "js";
  }

  return null;
};

let collectPreludeBlocks = (content) => {
  let lines = splitLines(content);
  let preludes = [];

  for (let i = 0; i < lines.length; i++) {
    if (!lines[i].startsWith("```res prelude")) {
      continue;
    }

    let start = i + 1;
    let end = start;
    while (end < lines.length && !lines[end].startsWith("```")) {
      end++;
    }

    preludes.push({
      line: i + 1,
      content: lines.slice(start, end).join("\n"),
    });

    i = end;
  }

  return preludes;
};

export let collectCodeTabPairs = (content) => {
  let lines = splitLines(content);
  let pairs = [];
  let warnings = [];
  let inTargetTab = false;
  let pendingRes = null;

  for (let i = 0; i < lines.length; i++) {
    let line = lines[i];

    if (line.includes('<CodeTab labels={["ReScript", "JS Output"]}>')) {
      inTargetTab = true;
      pendingRes = null;
      continue;
    }

    if (inTargetTab && line.includes("</CodeTab>")) {
      if (pendingRes != null) {
        warnings.push({
          line: pendingRes.line,
          message: "missing paired JS Output block",
        });
      }

      inTargetTab = false;
      pendingRes = null;
      continue;
    }

    if (!inTargetTab) {
      continue;
    }

    let kind = fenceKind(line);

    if (kind === "res-example") {
      let start = i + 1;
      let end = start;
      while (end < lines.length && !lines[end].startsWith("```")) {
        end++;
      }

      pendingRes = {
        line: i + 1,
        content: lines.slice(start, end).join("\n"),
      };
      i = end;
      continue;
    }

    if (kind === "js" && pendingRes != null) {
      let start = i + 1;
      let end = start;
      while (end < lines.length && !lines[end].startsWith("```")) {
        end++;
      }

      pairs.push({
        line: pendingRes.line,
        res: pendingRes,
        js: {
          content: lines.slice(start, end).join("\n"),
        },
      });

      pendingRes = null;
      i = end;
    }
  }

  return { pairs, warnings };
};

let stripCompilerBoilerplate = (output) => {
  let normalized = output.replace(
    /^\/\/ Generated by ReScript, PLEASE EDIT WITH CARE\n'use strict';\n\n/,
    "",
  );

  return normalized.replace(/\n\/\*.*\*\/\s*$/s, "").trimEnd();
};

let buildSnippetSource = ({ preludes, pair }) => {
  let visiblePreludes = preludes
    .filter((prelude) => prelude.line < pair.line)
    .map((prelude) => prelude.content)
    .filter(Boolean);

  return [...visiblePreludes, pair.res.content].filter(Boolean).join("\n\n");
};

let compileSnippet = (tempRoot, source) => {
  fs.writeFileSync(path.join(tempRoot, "src", "_tempFile.res"), source);
  child_process.execFileSync(
    "npm",
    ["exec", "rescript", "build", tempRoot, "--", "--quiet"],
    {
      cwd: projectRoot,
      stdio: "pipe",
    },
  );

  return fs.readFileSync(path.join(tempRoot, "src", "_tempFile.js"), "utf8");
};

let ensureTempProject = (tempRoot) => {
  fs.mkdirSync(path.join(tempRoot, "src"), { recursive: true });
  fs.writeFileSync(path.join(tempRoot, "rescript.json"), rescriptJson);
  fs.writeFileSync(path.join(tempRoot, "src", "_tempFile.res"), "");
  let tempNodeModules = path.join(tempRoot, "node_modules", "@rescript");
  let tempReactPackage = path.join(tempNodeModules, "react");
  if (!fs.existsSync(tempReactPackage)) {
    fs.mkdirSync(tempNodeModules, { recursive: true });
    fs.cpSync(
      path.join(projectRoot, "node_modules", "@rescript", "react"),
      tempReactPackage,
      { recursive: true },
    );
  }
};

export let run = ({
  docsRoot = path.join(projectRoot, "markdown-pages", "docs"),
  tempRoot = path.join(projectRoot, "temp"),
  logger = console,
} = {}) => {
  logger.log("Running tests...");
  ensureTempProject(tempRoot);

  let success = true;
  let warningCount = 0;

  globSync("{manual,react}/**/*.mdx", {
    cwd: docsRoot,
    absolute: true,
  }).forEach((file) => {
    let content = fs.readFileSync(file, { encoding: "utf-8" });
    let parsedResult = parseFile(content);
    if (parsedResult == null) {
      return;
    }

    fs.writeFileSync(path.join(tempRoot, "src", "_tempFile.res"), parsedResult);
    try {
      logger.log("testing examples in", file);
      child_process.execFileSync(
        "npm",
        ["exec", "rescript", "build", tempRoot, "--", "--quiet"],
        {
          cwd: projectRoot,
          stdio: "inherit",
        },
      );
    } catch {
      success = false;
      return;
    }

    let preludes = collectPreludeBlocks(content);
    let { pairs, warnings: malformedWarnings } = collectCodeTabPairs(content);

    for (let warning of malformedWarnings) {
      logger.warn(`${file}:${warning.line} ${warning.message}`);
      warningCount++;
    }

    for (let pair of pairs) {
      let snippetSource = buildSnippetSource({ preludes, pair });
      let compiledJs = compileSnippet(tempRoot, snippetSource);
      let expectedJs = stripCompilerBoilerplate(compiledJs);
      let currentJs = pair.js.content.trimEnd();

      if (expectedJs !== currentJs) {
        logger.warn(
          `${file}:${pair.line} JS Output is stale. Run scripts/test-examples.mjs --update`,
        );
        warningCount++;
      }
    }
  });

  return { success, warningCount };
};

if (process.argv[1] && path.resolve(process.argv[1]) === __filename) {
  let { success } = run();
  process.exit(success ? 0 : 1);
}
